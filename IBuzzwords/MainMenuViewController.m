#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];
    float fontSize = 36.0;
    NSString *fontName = @"Anton";
    self.playLabel.font = [UIFont fontWithName:fontName size:fontSize];
    self.buzzLabel.font = [UIFont fontWithName:fontName size:fontSize];
    self.settingsLabel.font = [UIFont fontWithName:fontName size:fontSize];
    self.rulesLabel.font = [UIFont fontWithName:fontName size:fontSize];
}

- (void)viewDidUnload
{
    [super viewDidUnload];

    // Release any retained subviews of the main view.
    self.playLabel = nil;
    self.buzzLabel = nil;
    self.settingsLabel = nil;
    self.rulesLabel = nil;
}

